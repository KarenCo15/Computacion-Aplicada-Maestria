#Dada una temperatura en grados celcius, obtener su equivalente en grados farenheit

print("Covirtiendo una temperatura de Celcius a Farenheit:\n")
celcius = int(input("Dame una temperatura en grados celcius:"))

farenheit = (celcius * 9/5 )+ 32
print(f"La temperatura {celcius} grados celcius equivale a {farenheit} grados farenheit\n")
